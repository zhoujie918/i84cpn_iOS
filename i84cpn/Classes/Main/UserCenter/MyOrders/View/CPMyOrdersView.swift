import UIKit

class CPMyOrdersView: CPTopTabBarView {
    override init(frame: CGRect) {
        super.init(frame: frame)
        setButtonTitle()
    }
    
    required init?(coder aDecoder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    func setButtonTitle() {
        button1.setTitle("待付款", forState: .Normal)
        button2.setTitle("已付款", forState: .Normal)
        button3.setTitle("已退订", forState: .Normal)
        button4.setTitle("已取消", forState: .Normal)
    }
}
